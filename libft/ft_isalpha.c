#include "libft.h"

int	ft_isalpha(int c)
{
	int isalpha;

	isalpha = 0;
	if (c >= 65 && c <= 90)
		isalpha = 1;
	if (c >= 97 && c <= 122)
		isalpha = 1;
	return (isalpha);
}
