#include "libft.h"

void	ft_putnbr(long n)
{
	if (n != -2147483648)
	{
		if (n < 0)
		{
			ft_putchar('-');
			n *= -1;
		}
		if (n >= 10)
			ft_putnbr(n / 10);
		ft_putchar((char)(n % 10 + '0'));
	}
	else
	{
		n = 147483648;
		ft_putchar('-');
		ft_putchar('2');
		ft_putnbr(n);
	}
}
